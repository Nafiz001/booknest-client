// Get latest books (sorted by addedDate)
export const getLatestBooks = (count = 6) => {
  return [...dummyBooks]
    .sort((a, b) => new Date(b.addedDate) - new Date(a.addedDate))
    .slice(0, count);
};

// Get books by category
export const getBooksByCategory = (category) => {
  return dummyBooks.filter(book => book.category === category);
};

// Get book by ID
export const getBookById = (id) => {
  return dummyBooks.find(book => book.id === parseInt(id));
};

// Get all published books
export const getPublishedBooks = () => {
  return dummyBooks.filter(book => book.status === 'published');
};
